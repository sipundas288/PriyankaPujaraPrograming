package twoDArray;

public class TwoArray {

	public static void main(String[] args) {
		int[] [] a=new int[2][2];
		a[0][0] =123;
		a[0][1]=34;
		a[1][0]=67;
		a[1][1]=89;
		
		System.out.println(a[0][0]);
		System.out.println(a[0][1]);
		System.out.println(a[1][0]);
		System.out.println(a[1][1]);
	}
}
