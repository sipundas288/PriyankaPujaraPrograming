package String;

public class RemoveVowels {

	public static void main(String[] args) {
		String value ="else";
		char[] ch = value.toCharArray();
		for(int i=0;i<ch.length;i++) {
			if(ch[i]!='a' && ch[i]!='e' &&  ch[i]!='i' &&  ch[i]!='o' &&  ch[i]!='u');
		System.out.println(ch[i]+" ");
		}

	}

}
